    // Method untuk Mengambil Data dari Database, tabel aset_masuk, disimpan ke instance AsetMasuk
     public ArrayList<AsetMasuk> getAsetMasukList(String keyword){
        ArrayList<AsetMasuk> asetMasukList = new ArrayList<AsetMasuk>();
        DBConnection koneksi = new DBConnection();
        Connection connection =koneksi.getConnection();
        AsetMasuk asetMasuk;
        
        String query = "SELECT * FROM aset_masuk " + keyword;
        Statement st;
        ResultSet rs;
        
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()){
                asetMasuk = new AsetMasuk(
                        rs.getInt("kode_aset"),
                        rs.getString("nama_aset"),
                        rs.getInt("jumlah"),
                        rs.getString("tanggal_masuk"),
                        rs.getString("kategori"));
                asetMasukList.add(asetMasuk);
            }
        } catch(SQLException | NullPointerException ex) {
            System.err.println("Koneksi Null Gagal");
        }
        return asetMasukList;
    }
    
    
    // Method untuk Menampilkan Data yang Diambil dari method getAsetMasukList ke tAsetMasuk yang ada di Program
    public void selectAsetMasuk(String keyword){
        ArrayList<AsetMasuk> list = getAsetMasukList(keyword);
        DefaultTableModel model = (DefaultTableModel)tAsetMasuk.getModel();
        Object[] row = new Object[5];// 5 karena ada 5 field (kolom)
        
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getKode();     // 0  ->   Field Ke-1
            row[1] = list.get(i).getNama();
            row[2] = list.get(i).getJumlah();
            row[3] = list.get(i).getTanggal();
            row[4] = list.get(i).getKategori();
            
            model.addRow(row);
        }
    }
    
    // Method untuk me-Refresh Tampilan Table
    // Reset Table Aset Masuk
    public final void resetTableMasuk(String keyword){
        DefaultTableModel model = (DefaultTableModel)tAsetMasuk.getModel();
        model.setRowCount(0);
        selectAsetMasuk(keyword);
    }
    
