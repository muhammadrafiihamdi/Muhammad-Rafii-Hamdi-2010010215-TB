    private void bAsetMasukActionPerformed(ActionEvent evt) {//GEN-FIRST:event_bAsetMasukActionPerformed
        // TODO add your handling code here:
        AsetMasuk asetMasuk = new AsetMasuk();
        asetMasuk.setNama(namaAset.getText());
        asetMasuk.setJumlah(Integer.parseInt(jumlah.getText()));
        asetMasuk.setTanggal(tanggalMasuk.getText());
        asetMasuk.setKategori(kategori.getSelectedItem().toString());
        
        if(asetMasuk.getNama().equalsIgnoreCase("") ||
           asetMasuk.getJumlah()==0 ||
           asetMasuk.getTanggal().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Lengkapi Data");
        } else {
            DBConnection koneksi = new DBConnection();
            Connection con = koneksi.getConnection();
            PreparedStatement ps;
            
            try {
                String qry = "INSERT INTO aset_masuk VALUES (NULL,?,?,?,?)";
                ps = con.prepareStatement(qry);
                ps.setString(1, asetMasuk.getNama());
                ps.setInt(2, asetMasuk.getJumlah());
                ps.setString(3, asetMasuk.getTanggal());
                ps.setString(4, asetMasuk.getKategori());
                ps.executeUpdate();
                System.out.println("Data Aset Masuk Berhasil Ditambahkan");
                resetTableMasuk("");
            } catch (Exception e) {
                System.out.println("Error Tombol Tambah Aset Masuk : " + e);
            }
        }
    }//GEN-LAST:event_bAsetMasukActionPerformed

